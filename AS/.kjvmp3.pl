	my($sec, $chap, $next) = @_;
	(my $book = $chap) =~ s/ Chapter.*//;
	# warn "config $sec $chap $next\n";
	
	# book closing statement runs longer past the text
	$next =~ /^$book / or return $sec - 7;

	# 2 second silent tail
	return $sec - 2;
    },
